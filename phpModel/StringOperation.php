<?php
/*------------------------------------------------------------
* intro: 该函数用于将字符串中的敏感词汇比如 '\' '"' 转化为转义后的字符
*        先用str_replace转换\, 避免下划线被多次转换
* return 转换后的字符串
------------------------------------------------------------*/
function str_convert($str){
  $str = str_replace('\\', '\\\\', $str);
  $str = str_replace('"', '\"', $str);

  return $str;
}
